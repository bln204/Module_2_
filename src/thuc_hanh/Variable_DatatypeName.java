package thuc_hanh;

public class Variable_DatatypeName {
    public static void main(String[] args) {
        int a = 10;
        float b = 20.5f;
        double c = 30.5;
        boolean d = true;
        char e = 'c';
        String s = "Hà Nội";
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(s);

    }
}
